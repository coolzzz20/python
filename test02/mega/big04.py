number1 = input("숫자입력>>")
number2 = input("숫자입력>>")
number11 = int(number1)
number22 = int(number2)
print("-----------")
print(number1, "과", number2, "합은",number11 + number22,"입니다.")
print(number1, "과", number2, "곱은", number11*number22,"입니다.")
print(number1, "과", number2, "차는", number11-number22, "입니다.")


#숫자와 문자를 기호 없이 사용할 때 그냥 사용해도 된다.
#number1 과 number2 = number11 과 number22 나오는게 같다.

